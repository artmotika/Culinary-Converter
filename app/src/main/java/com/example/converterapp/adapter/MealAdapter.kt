    @SuppressLint("SetTextI18n")
    override fun onBindViewHolder(mealViewHolder: MealViewHolder, i: Int) {
        mealViewHolder.imageMeal.setImageResource(pages[i].img)
        when (isGram) {
            true -> {
                mealViewHolder.textMealRus.text = volumeRussianTextArray[i]
                mealViewHolder.textMeal.text =
                    "%.2f".format(editNumber * constantsArrayVolume[mealId - 1][i]) + " " + pages[i].title
            }
            false -> {
                mealViewHolder.textMealRus.text = weightRussianTextArray[i]
                mealViewHolder.textMeal.text =
                    "%.2f".format(editNumber / constantsArrayWeight[mealId - 1][i]) + " " + pages[i].title
            }
        }
    }

